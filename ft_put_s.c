#include "ft_printf.h"

int	ft_put_s(char *s, int len)
{
	int	i;

	i = -1;
	if (!s)
	{
		write (1, "(null)", 6);
		return (len + 6);
	}
	while (s[++i])
	{
		write(1, &s[i], 1);
		len++;
	}
	return (len);
}
